# Master list — flatten + deduplicate
# ---------------------------------------------------------------------------

def _build_all_words() -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for category in [
        _GENERIC_INSULTS,
        _OBSCENE,
        _FAMILY_INSULTS,
        _ANIMAL_INSULTS,
        _CURSES,
        _SLURS,
        _THREATS,
        _SPAM_PROFANE,
    ]:
        for word in category:
            w = word.strip()
            if w and w not in seen:
                seen.add(w)
                result.append(w)
    return result


